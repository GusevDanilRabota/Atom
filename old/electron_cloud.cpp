/**
 * @file electron_cloud.cpp
 * @brief Моделирование электронного облака атома водорода.
 *
 * @details Программа вычисляет плотность вероятности нахождения электрона
 * в атоме водорода для заданных квантовых чисел n и l (при m=0).
 * Строится двумерное сечение (плоскость y=0) и сохраняется в виде
 * графического файла формата PPM (Portable Pixmap).
 * Используются только стандартные средства C++11 и системные функции Windows
 * для корректной работы с консолью (кириллица).
 *
 * Физическая основа:
 * В атомных единицах (боровский радиус a0 = 1, заряд ядра Z = 1)
 * волновая функция электрона имеет вид:
 *   ψ(r,θ,φ) = R_{nl}(r) · Y_{lm}(θ,φ)
 * Плотность вероятности (электронная плотность) – это |ψ|².
 *
 * Аналогия с радиотехникой:
 *   - волновая функция ψ аналогична комплексной амплитуде сигнала,
 *   - |ψ|² – мгновенная мощность (интенсивность) этого сигнала.
 *
 * @author (ваше имя)
 * @date 2026-05-19
 * @version 2.0
 */

#include <algorithm> // std::min
#include <math.h>    // математические функции: exp, sqrt, fabs
#include <stdio.h> // ввод/вывод C: printf, FILE, fopen, fputc, fclose, fgets, sscanf
#include <stdlib.h> // общие утилиты (не используется, но для порядка)
#include <string.h> // strlen (для обрезки строки fgets)
#include <vector>   // динамический массив для пикселей

#ifdef _WIN32
#include <windows.h> // SetConsoleOutputCP, SetConsoleCP – для русского языка в консоли
#endif

// Константа π, так как M_PI может отсутствовать в строгих режимах
#ifndef M_PI
#define M_PI 3.14159265358979323846
#endif

// ----------------------------------------------------------------------
// Вспомогательные функции безопасного ввода
// ----------------------------------------------------------------------

/**
 * @brief Считывает строку из стандартного ввода и удаляет завершающий символ
 * новой строки.
 *
 * @param buffer Буфер для строки.
 * @param size Размер буфера.
 * @return true если чтение успешно, false при ошибке или EOF.
 */
bool safe_gets(char *buffer, int size) {
  if (fgets(buffer, size, stdin) == NULL) {
    return false;
  }
  // Удаляем '\n' в конце, если он есть
  size_t len = strlen(buffer);
  if (len > 0 && buffer[len - 1] == '\n') {
    buffer[len - 1] = '\0';
  }
  return true;
}

// ----------------------------------------------------------------------
// Физические функции
// ----------------------------------------------------------------------

/**
 * @brief Радиальная компонента волновой функции водорода R_{nl}(r).
 *
 * @details Вычисляется по аналитическим формулам для низших состояний.
 * Радиальная функция нормирована так, что ∫_0^∞ |R(r)|² r² dr = 1.
 * В коде реализованы три состояния:
 *   - 1s (n=1, l=0): \f$ R_{10}(r) = 2e^{-r} \f$
 *   - 2s (n=2, l=0): \f$ R_{20}(r) = \frac{1}{2\sqrt{2}}(2 - r)e^{-r/2} \f$
 *   - 2p (n=2, l=1): \f$ R_{21}(r) = \frac{1}{2\sqrt{6}} r e^{-r/2} \f$
 *
 * Здесь r – расстояние от ядра в единицах боровского радиуса (a0 ≈ 0.529 Å).
 *
 * @param n Главное квантовое число (n >= 1).
 * @param l Орбитальное квантовое число (0 <= l < n).
 * @param r Расстояние от ядра, безразмерное (в единицах a0).
 * @return Значение радиальной волновой функции R_{nl}(r).
 *
 * @note Для невозбуждённых состояний (n>2) возвращается 0; программу
 * легко расширить, добавив соответствующие формулы из учебников.
 */
double radial(int n, int l, double r) {
  if (n == 1 && l == 0) {
    return 2.0 * exp(-r);
  } else if (n == 2 && l == 0) {
    return (1.0 / (2.0 * sqrt(2.0))) * (2.0 - r) * exp(-r / 2.0);
  } else if (n == 2 && l == 1) {
    return (1.0 / (2.0 * sqrt(6.0))) * r * exp(-r / 2.0);
  }
  return 0.0;
}

/**
 * @brief Угловая часть волновой функции (сферическая гармоника Y_{l0}).
 *
 * @details При m = 0 сферическая гармоника зависит только от полярного угла θ
 * и не зависит от азимутального угла φ.
 * Используются вещественные нормированные функции:
 *   - l=0 (s-орбиталь): \f$ Y_{00}(\theta) = \frac{1}{\sqrt{4\pi}} \f$
 *   - l=1 (p-орбиталь, m=0): \f$ Y_{10}(\theta) = \sqrt{\frac{3}{4\pi}}
 * \cos\theta \f$
 *
 * Аргумент передаётся как cos(θ) = z/r для удобства вычислений.
 *
 * @param l Орбитальное квантовое число.
 * @param cos_theta Косинус полярного угла (z/r).
 * @return Значение угловой части \f$ Y_{l0}(\theta) \f$.
 *
 * @note Для l > 1 не реализовано, при необходимости легко добавить
 * выражения через полиномы Лежандра.
 */
double angular(int l, double cos_theta) {
  if (l == 0) {
    return 1.0 / sqrt(4.0 * M_PI);
  } else if (l == 1) {
    return sqrt(3.0 / (4.0 * M_PI)) * cos_theta;
  }
  return 0.0;
}

/**
 * @brief Волновая функция ψ(x,y,z) для квантовых чисел (n, l, m=0).
 *
 * @details Собирает полную волновую функцию из радиальной и угловой частей:
 *   ψ(r,θ) = R_{nl}(r) · Y_{l0}(θ)
 *
 * Расчёт выполняется в декартовых координатах:
 *   - расстояние от ядра \f$ r = \sqrt{x^2 + y^2 + z^2} \f$
 *   - \f$ \cos\theta = z/r \f$ (при условии, что r > 0)
 *
 * Если точка близка к началу координат (r → 0), то:
 *   - для s-состояний ψ конечно, так как радиальная часть не обращается в ноль;
 *   - для остальных (l > 0) ψ стремится к нулю (центробежный барьер).
 *
 * @param n Главное квантовое число.
 * @param l Орбитальное квантовое число.
 * @param x,x,y,z Декартовы координаты точки (в единицах боровского радиуса).
 * @return Значение волновой функции ψ(x,y,z).
 *
 * @note Волновая функция действительна, так как выбрано m=0 и используются
 * действительные сферические гармоники. Для комплексных случаев потребовалось
 * бы возвращать комплексное число.
 */
double wave_function(int n, int l, double x, double y, double z) {
  double r = sqrt(x * x + y * y + z * z);
  if (r < 1e-12) {
    if (l == 0) {
      return radial(n, l, 0.0) * angular(l, 1.0);
    } else {
      return 0.0;
    }
  }
  double cos_theta = z / r;
  return radial(n, l, r) * angular(l, cos_theta);
}

