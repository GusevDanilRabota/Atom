/**
 * @brief Точка входа в программу моделирования электронного облака.
 *
 * @details Алгоритм работы:
 *   1. Настройка консоли для корректного отображения русского текста (Windows).
 *   2. Запрос у пользователя квантовых чисел n, l с надёжным построчным вводом.
 *   3. Задание области моделирования (диапазоны по X и Z) и размера
 * изображения.
 *   4. Вычисление значений |ψ|² в каждом пикселе сечения y=0.
 *   5. Поиск максимального значения плотности вероятности для нормировки.
 *   6. Масштабирование значений в диапазон яркостей [0, 255].
 *   7. Сохранение результата в бинарном PPM-файле (формат P6).
 *
 * Формат PPM P6:
 *   - Текстовый заголовок: "P6\n<ширина> <высота>\n255\n"
 *   - Далее бинарные данные: тройки байт (R,G,B) для каждого пикселя.
 *
 * В нашем случае изображение делается серым (R=G=B).
 *
 * @return 0 при успешном завершении, 1 при ошибках ввода/вывода.
 */
int main() {
  // ---------- Настройка консоли для русского языка ----------
#ifdef _WIN32
  SetConsoleOutputCP(65001); // Устанавливаем кодировку UTF-8 для вывода
  SetConsoleCP(65001);       // и для ввода
#endif

  // ---------- Параметры расчёта ----------
  int n = 1;
  int l = 0;               ///< Главное и орбитальное квантовые числа
  double xmin = -8.0;
  double xmax = 8.0; ///< Границы визуализации по оси X (в a0)
  double zmin = -8.0;
  double zmax = 8.0; ///< Границы визуализации по оси Z (в a0)
  int width = 600;
  int height = 600;  ///< Разрешение выходного изображения

  // Вспомогательный буфер для построчного ввода
  char input_buffer[256];

  // ---------- Ввод квантовых чисел ----------
  printf("Моделирование электронного облака атома водорода\n");
  printf("(Атомные единицы: координаты в единицах a0 = 0.529 Å)\n\n");

  while (true) {
    printf("Введите n и l (например 1 0 для 1s): ");
    if (!safe_gets(input_buffer, sizeof(input_buffer))) {
      printf("Ошибка чтения ввода.\n");
      return 1;
    }
    // Парсим два целых числа из строки
    if (sscanf(input_buffer, "%d %d", &n, &l) == 2) {
      if (l >= n) {
        printf("Ошибка: l должно быть меньше n (l < n). Попробуйте ещё раз.\n");
        continue;
      }
      break; // успешный ввод
    } else {
      printf("Ошибка: требуется два целых числа. Попробуйте ещё раз.\n");
    }
  }

  // ---------- Ввод границ по X ----------
  while (true) {
    printf("Введите границы по X (xmin xmax): ");
    if (!safe_gets(input_buffer, sizeof(input_buffer))) {
      printf("Ошибка чтения ввода.\n");
      return 1;
    }
    if (sscanf(input_buffer, "%lf %lf", &xmin, &xmax) == 2) {
      if (xmin >= xmax) {
        printf("Ошибка: xmin должно быть меньше xmax. Попробуйте ещё раз.\n");
        continue;
      }
      break;
    } else {
      printf("Ошибка: требуется два вещественных числа. Попробуйте ещё раз.\n");
    }
  }

  // ---------- Ввод границ по Z ----------
  while (true) {
    printf("Введите границы по Z (zmin zmax): ");
    if (!safe_gets(input_buffer, sizeof(input_buffer))) {
      printf("Ошибка чтения ввода.\n");
      return 1;
    }
    if (sscanf(input_buffer, "%lf %lf", &zmin, &zmax) == 2) {
      if (zmin >= zmax) {
        printf("Ошибка: zmin должно быть меньше zmax. Попробуйте ещё раз.\n");
        continue;
      }
      break;
    } else {
      printf("Ошибка: требуется два вещественных числа. Попробуйте ещё раз.\n");
    }
  }

  // ---------- Ввод разрешения ----------
  while (true) {
    printf("Введите ширину и высоту изображения: ");
    if (!safe_gets(input_buffer, sizeof(input_buffer))) {
      printf("Ошибка чтения ввода.\n");
      return 1;
    }
    if (sscanf(input_buffer, "%d %d", &width, &height) == 2) {
      if (width <= 0 || height <= 0) {
        printf("Ошибка: размеры должны быть положительными. Попробуйте ещё "
               "раз.\n");
        continue;
      }
      break;
    } else {
      printf("Ошибка: требуется два целых числа. Попробуйте ещё раз.\n");
    }
  }

  // ---------- Вычисление плотности вероятности на сетке ----------
  // Храним значения |ψ|² в одномерном массиве (построчная развёртка)
  std::vector<double> pixels(static_cast<size_t>(width) *
                             static_cast<size_t>(height));
  double max_prob = 0.0; // для последующей нормировки яркости

  // Внешний цикл по строкам (изменение координаты z)
  for (int row = 0; row < height; ++row) {
    double z = zmin + (zmax - zmin) * row / (height - 1);
    for (int col = 0; col < width; ++col) {
      double x = xmin + (xmax - xmin) * col / (width - 1);
      double psi = wave_function(n, l, x, 0.0, z);
      double prob = psi * psi;
      pixels[row * width + col] = prob;
      if (prob > max_prob) {
        max_prob = prob;
      }
    }
  }

  // Защита от случая, когда вся область нулевая
  if (max_prob < 1e-15) {
    max_prob = 1.0;
  }

  // ---------- Запись изображения в формате PPM ----------
  FILE *f = fopen("electron_cloud.ppm", "wb");
  if (!f) {
    perror("Не удалось создать файл");
    return 1;
  }

  fprintf(f, "P6\n%d %d\n255\n", width, height);

  // Преобразование вероятности в градации серого
  for (double prob : pixels) {
    double scaled = (prob / max_prob) * 255.0;
    unsigned char val = static_cast<unsigned char>(std::min(255.0, scaled));
    fputc(val, f); // R
    fputc(val, f); // G
    fputc(val, f); // B
  }
  fclose(f);

  printf("Изображение сохранено в electron_cloud.ppm\n");
  printf("Максимальная плотность вероятности: %e\n", max_prob);
  printf("Подсказка: откройте файл PPM в GIMP, IrfanView или браузере.\n");

  return 0;
}